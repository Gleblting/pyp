# Ввод чисел
a = int(input("Введите первое число: "))
b = int(input("Введите второе число: "))

# Функция для нахождения НОД
def gcd(x, y):
    while y:
        x, y = y, x % y
    return x

# Вычисление НОД
result = gcd(a, b)

# Вывод результата
print("Наибольший общий делитель:", result)