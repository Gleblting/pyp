
def remove_duplicates(lst):
    """Функция для удаления дубликатов из списка."""
    seen = set()  # Множество для хранения уникальных элементов
    unique_list = []  # Новый список для уникальных элементов
    for item in lst:  # Проходим по элементам входного списка
        if item not in seen:  # Проверяем, есть ли элемент в множестве
            seen.add(item)  # Добавляем элемент в множество
            unique_list.append(item)  # Добавляем элемент в новый список
    return unique_list  # Возвращаем список без дубликатов

# Пример использования
input_list = [1, 2, 3, 2, 4, 1, 5, 3]
print("Список без дубликатов:", remove_duplicates(input_list))  # Вывод: Список без дубликатов: [1, 2, 3, 4, 5]